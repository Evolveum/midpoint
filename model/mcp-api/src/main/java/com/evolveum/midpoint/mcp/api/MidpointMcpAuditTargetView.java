package com.evolveum.midpoint.mcp.api;

/** Target object reference; {@code type} is the MCP REST collection name when known (e.g. {@code users}). */
public class MidpointMcpAuditTargetView {

    private String oid;
    private String type;
    private String name;

    public String getOid() {
        return oid;
    }

    public void setOid(String oid) {
        this.oid = oid;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
